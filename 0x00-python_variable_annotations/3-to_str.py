#!/usr/bin/env python3
"""
    Convert a floating-point number to a string representation.
"""


def to_str(n: float) -> str:
    """
    Convert a floating-point number to a string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the floating-point number.
    """
    return str(n)
