#!/usr/bin/env python3
"""
    Adds two float and returns the sum.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns the sum.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of the two floats.
    """
    return a+b
